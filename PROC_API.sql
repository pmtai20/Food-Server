CREATE PROC sp_DonHang_ChiTiet
@MAKH VARCHAR(10),
@MADH VARCHAR(10),
@USER_TYPE VARCHAR(10)
AS
	BEGIN
	IF @USER_TYPE = '3'
		BEGIN
		IF EXISTS(SELECT MADH FROM DON_DH WHERE MADH=@MADH AND MACN=@MAKH)
		select tddh.MAMONAN, tddh.TENMONAN, tddh.GIABAN GIA, tddh.SOLUONG from   THUCDONDATHANG tddh 
		WHERE tddh.MADH=@MADH
		ELSE
		RAISERROR ('KHONG TON TAI KHACH HANG',16,1)
		END
	ELSE IF @USER_TYPE = '4'
		BEGIN
		IF EXISTS(SELECT MADH FROM DON_DH WHERE MADH=@MADH AND MATX=@MAKH)
		select tddh.MAMONAN, tddh.TENMONAN, tddh.GIABAN GIA, tddh.SOLUONG  from  THUCDONDATHANG tddh 
		WHERE  tddh.MADH = @MADH
		ELSE
		RAISERROR ('KHONG TON TAI KHACH HANG',16,1)
		END
	ELSE IF @USER_TYPE = '5'
		BEGIN
		IF EXISTS(SELECT MADH FROM DON_DH WHERE MADH=@MADH AND MAKH=@MAKH)
		select tddh.MAMONAN, tddh.TENMONAN, tddh.GIABAN GIA, tddh.SOLUONG from THUCDONDATHANG tddh
		WHERE  tddh.MADH = @MADH
		ELSE
		RAISERROR ('KHONG TON TAI KHACH HANG',16,1)
		END

	ELSE
	RAISERROR ('KHONG TON TAI KHACH HANG',16,1)
	END
GO
-- LẤY ĐƠN HÀNG TÀI XẾ
CREATE PROC sp_DonHang_TX
@MATX VARCHAR(10),
@TGBD DATETIME,
@TGKT DATETIME
AS
	IF EXISTS(SELECT MATX FROM TAIXE WHERE MATX=@MATX)
	BEGIN
	IF(@TGBD=NULL)
	BEGIN
		SET @TGBD =(SELECT NGAYTG FROM TAIXE WHERE MATX=@MATX)
		SELECT TOP(10) MADH,TENTX,TRANGTHAIDH,PHISP FROM DON_DH WHERE NGAYLAP > @TGBD AND MATX=@MATX
		ORDER BY NGAYLAP 
		RETURN
	END
	ELSE
	SELECT MADH,TENTX,TRANGTHAIDH,PHISP FROM DON_DH WHERE NGAYLAP BETWEEN @TGBD AND @TGKT AND MATX=@MATX
	ORDER BY NGAYLAP 
	END

	ELSE
	RAISERROR ('KHONG TON TAI TAI XE',16,1)
GO
