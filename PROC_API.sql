CREATE PROC sp_DonHang_CN_XuLy
@MACN VARCHAR(10),
@TRANGTHAIDH CHAR(20),
@TGBD DATETIME,
@TGKT DATETIME
AS
	IF(@TGBD=NULL)
	BEGIN
		SET @TGBD= (SELECT NGAYTG FROM CHINHANH WHERE MACN=@MACN)
		SELECT  * FROM DON_DH DH 
		WHERE @TGBD<NGAYLAP AND DH.MACN=@MACN AND DH.TRANGTHAIDH=@TRANGTHAIDH
		order by NGAYLAP
	END
	ELSE
	SELECT * FROM DON_DH DH 
	WHERE  DH.MACN=@MACN AND DH.TRANGTHAIDH=@TRANGTHAIDH
	order by NGAYLAP
	
GO
-- LẤY ĐƠN HÀNG CHI NHANH
CREATE PROC sp_DonHang_CN
@MACN VARCHAR(10),
@TGBD DATETIME,
@TGKT DATETIME,
@TRANGTHAIDH CHAR(20)
AS
	IF EXISTS(SELECT MACN FROM CHINHANH WHERE MACN=@MACN)
	BEGIN
	IF @TRANGTHAIDH='ALL'
	BEGIN
	SELECT * FROM DON_DH DH 
	WHERE DH.NGAYLAP BETWEEN @TGBD AND @TGKT AND DH.MACN=@MACN 
	ORDER BY DH.NGAYLAP DESC
	SELECT COUNT(MADH)SOLUONG FROM DON_DH WHERE  NGAYLAP BETWEEN @TGBD AND @TGKT AND  MACN=@MACN 
	END
	ELSE
	BEGIN
	SELECT * FROM DON_DH DH 
	WHERE DH.NGAYLAP BETWEEN @TGBD AND @TGKT AND DH.MACN=@MACN  AND DH.TRANGTHAIDH=@TRANGTHAIDH
	ORDER BY DH.NGAYLAP DESC
	SELECT COUNT(MADH) SOLUONG,SUM(PHISP) DOANHTHU FROM DON_DH WHERE  NGAYLAP BETWEEN @TGBD AND @TGKT AND  MACN=@MACN  AND TRANGTHAIDH=@TRANGTHAIDH
	END
	END
	ELSE
	RAISERROR ('KHONG TON TAI CHI NHANH',16,1)
GO
