CREATE PROC sp_Tao_DH
@HINHTHUCTT CHAR(20),
@MADIACHI VARCHAR(10),
@MAKH VARCHAR(10),
@MACN VARCHAR(10),
@PHIVANCHUYEN INT,
@list_MONAN list_MONAN READONLY
--@MONAN TABLE(MAMONAN VARCHAR(10),SOLUONG INT)
AS
BEGIN TRAN
	IF (@HINHTHUCTT='CHUYEN KHOAN' OR @HINHTHUCTT='VI' OR @HINHTHUCTT='NGAN HANG')
	BEGIN
		SELECT *FROM @list_MONAN
		IF NOT EXISTS(SELECT MACN FROM CUAHANG WHERE MACN=@MACN AND TINHTRANGCH ='AVAILABLE')
		BEGIN
			ROLLBACK TRAN
			RAISERROR ('CUA HANG KHONG HOAT DONG',16,1)
			RETURN
		END
		DECLARE @SODUVI INT,@TENKH VARCHAR(50),@SDTKH VARCHAR(10)
		SELECT @SODUVI=SODUVI,@TENKH=TEN,@SDTKH=SDT FROM KHACHHANG WHERE MAKH=@MAKH

		DECLARE @TENCN VARCHAR(80),@DIACHICN VARCHAR (50),@PTHH INT,@MADT VARCHAR(10)
		DECLARE @MADH INT
		EXEC sp_IDMAX 'DON_DH',@MADH OUT
		--IF NOT EXISTS (SELECT MADIACHI FROM DIACHI WHERE MADIACHI=@MADIACHI)
		--BEGIN
		--	ROLLBACK TRAN
		--	RAISERROR ('KHONG TON TAI DIA CHI',16,1)
		--	RETURN
		--END
		DECLARE @MAKHUVUC VARCHAR(10) = NULL-- (SELECT KV.MAKHUVUC FROM DIACHI DC JOIN PHUONG P ON DC.MAPHUONG=P.MAPHUONG JOIN QUANHUYEN QH ON QH.MAHUYEN=P.MAHUYEN JOIN KHUVUC KV ON QH.MAKHUVUC=KV.MAKHUVUC WHERE MADC=DC.MADC)
		DECLARE @NGAYTAO DATETIME=GETDATE()
		EXEC sp_ThemDonDH @MADH,'AVAILABLE',@NGAYTAO,@HINHTHUCTT,0,@PHIVANCHUYEN, @MAKH,@TENKH,@SDTKH,@MADIACHI,NULL, @MACN,@TENCN,@DIACHICN,@MADT,@PTHH,NULL,NULL,NULL,NULL,@MAKHUVUC
		IF @@ERROR<>0
		BEGIN
			ROLLBACK TRAN
			RAISERROR ('KHONG TAO DUOC DON HANG',16,1)
			RETURN
		END
		
		DECLARE @STT INT =(SELECT MAX(STT) FROM @list_MONAN)
		DECLARE @I INT =1
		DECLARE @THUCDON temp_THUCDON
		DECLARE @TONGTIEN INT=0
		INSERT INTO @THUCDON SELECT MAMONAN,TENMONAN,GIA FROM THUCDON WHERE MACN=@MACN
		WHILE (@I<=@STT)
		BEGIN
			DECLARE @MAMONAN VARCHAR(10)
			DECLARE @SOLUONG INT,@GIA INT, @TENMONAN VARCHAR(50)
			SELECT @MAMONAN=MAMONAN,@SOLUONG=SOLUONG FROM @list_MONAN WHERE STT=@I
			SELECT @TENMONAN=TENMONAN,@GIA=GIA FROM @THUCDON WHERE MAMONAN=@MAMONAN
			SELECT *FROM @THUCDON
			EXEC sp_ThemThucDonDatHang @MAMONAN,@TENMONAN,@MADH,@GIA,@SOLUONG,@NGAYTAO
			IF @@ERROR <>0
			BEGIN
			ROLLBACK TRAN
			RAISERROR ('KHONG TAO DUOC DON HANG',16,1)
			RETURN
			END
			SET @TONGTIEN=@TONGTIEN+@GIA*@SOLUONG
			SET @I=@I+1
		END
		UPDATE DON_DH SET PHISP=@TONGTIEN WHERE MADH=@MADH
		IF (@HINHTHUCTT='VI')
		BEGIN
			
			UPDATE KHACHHANG SET SODUVI=@SODUVI- @TONGTIEN-@PHIVANCHUYEN WHERE MAKH=@MAKH
			IF @@ERROR <>0
			BEGIN
			ROLLBACK TRAN
			RAISERROR ('SO DU KHONG DU DE THANH TOAN',16,1)
			RETURN
			END
		END
	END
	ELSE
	BEGIN
		ROLLBACK TRAN
		RAISERROR ('HINH THUC THANH TOAN KHONG HOP LE',16,1)
		RETURN
	END

COMMIT TRAN
GO
