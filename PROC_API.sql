CREATE 
PROC sp_DANGNHAP
	@LOAITK INT,
	@TENTK NVARCHAR(50),
	@MATKHAUTK NVARCHAR(50)

AS
BEGIN TRAN
IF @LOAITK<0 OR @LOAITK>5
	BEGIN
		RAISERROR (N'KHÔNG TỒN TẠI NGƯỜI DÙNG',16,1);
		ROLLBACK
		RETURN
	END
IF NOT EXISTS( SELECT TENTK FROM TAIKHOAN WHERE LOAITK=@LOAITK AND TENTK=@TENTK AND @MATKHAUTK=MATKHAUTK AND TINHTRANGTK='AVAILABLE')
BEGIN
		RAISERROR (N'TEN TK HOAC MATKHAU DANG NHAP KHONG DUNG',16,1);
		ROLLBACK
		RETURN
END
IF @LOAITK=0
	BEGIN
		SELECT QTV.MAQTV FROM QUANTRIVIEN QTV JOIN TAIKHOAN TK ON QTV.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK
		COMMIT TRAN
		RETURN
	END
IF @LOAITK=1
BEGIN
		SELECT NV.MANHANVIEN FROM NHANVIEN NV JOIN TAIKHOAN TK ON NV.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK
		COMMIT TRAN
		RETURN
END

IF @LOAITK=2
BEGIN
		SELECT DT.MADOITAC,DT.NGAYTG FROM DOITAC DT JOIN TAIKHOAN TK ON DT.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK
		COMMIT TRAN
		RETURN
END
IF @LOAITK=3
BEGIN
		SELECT CN.MACN,CN.NGAYTG FROM CHINHANH CN JOIN TAIKHOAN TK ON CN.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK
		COMMIT TRAN
		RETURN
END
IF @LOAITK=4
BEGIN
		SELECT TX.MATX,TX.NGAYTG FROM TAIXE TX JOIN TAIKHOAN TK ON TX.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK AND TK.NGAYTG=TX.NGAYTG
		COMMIT TRAN
		RETURN
END

IF @LOAITK=5
BEGIN
		SELECT KH.MAKH,KH.NGAYTG FROM KHACHHANG KH JOIN TAIKHOAN TK ON KH.MATK=TK.MATK WHERE TK.TENTK=@TENTK AND TK.MATKHAUTK=@MATKHAUTK AND TK.NGAYTG=KH.NGAYTG
		COMMIT TRAN
		RETURN
END
GO	
